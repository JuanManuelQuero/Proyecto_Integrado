    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request, Mobile $mobile)
    {
        $comment = new Comment();

        if(strlen($request->comentario) == 0) {
            return redirect()->route('mobiles.index')->with('mensaje', 'El comentario no puede estar vacÃ­o');
        } else {
            $comment->comentario = $request->comentario;
            $comment->parent_id = $request->parent_id;
            $comment->user_id = \auth()->id();

            $mobile->comments()->save($comment);
            return redirect()->route('mobiles.index')->with('mensaje', 'Comentario publicado');
        }
    }

